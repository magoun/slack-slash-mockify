package flagify

import (
	"strings"
	"unicode"
)

func Flagify(data []string) string {
	count := 0
	message := ""
	text := strings.Join(data, " ")

	for count < len(text) {
		if char := rune(text[count]); !unicode.IsLetter(char) {
			message += string(char)
			count++
		} else if count+2 < len(text) {
			val, found := mkFlag(text[count : count+2])
			if found {
				count += 2
			} else {
				count++
			}

			message += val
		} else {
			message += ":alphabet-white-" + strings.ToLower(string(text[count])) + ":"
			count++
		}
	}

	return message
}

func mkFlag(chunk string) (string, bool) {
	if find(Flags(), strings.ToUpper(chunk)) != -1 {
		return ":flag-" + chunk + ":", true
	} else {
		return ":alphabet-white-" + strings.ToLower(strings.Split(chunk, "")[0]) + ":", false
	}
}

func find(slice []string, val string) int {
	for i, v := range slice {
		if v == val {
			return i
		}
	}

	return -1
}

